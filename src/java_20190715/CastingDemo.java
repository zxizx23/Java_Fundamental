package java_20190715;

public class CastingDemo {
	public static void main(String[] args) {
		byte b1 = (byte)250;
		System.out.println(b1);
		
		int i1 = (int)123.45;
		System.out.println(i1);
		
		byte a = 10;
		byte b = 20;
		byte c = (byte)(a+b);
		System.out.println(c);
		
	}

}

